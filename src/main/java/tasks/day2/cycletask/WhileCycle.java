/*        - используя while вывести все числа от 0 до 20 в 1 строку через пробел*/

package main.java.tasks.day2.cycletask;

public class WhileCycle {
    public static void main(String[] args) {
        int i = 0;
        while (i < 21){
            System.out.println(i);
            i++;
        }
    }
}
